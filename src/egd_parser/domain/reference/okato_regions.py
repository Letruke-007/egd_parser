def detect_okato_series_prefix_hints(issued_by: str | None) -> dict[str, int]:
    if not issued_by:
        return {}
    normalized = normalize_region_text(issued_by)
    scores: dict[str, int] = {}
    for marker, codes in OKATO_REGION_HINTS.items():
        if marker not in normalized:
            continue
        for index, code in enumerate(codes):
            weight = 3 if index == 0 else 1
            scores[code] = max(scores.get(code, 0), weight)
    return scores


def normalize_region_text(value: str) -> str:
    normalized = value.lower().replace("ё", "е")
    normalized = re.sub(r"[^a-zа-я0-9]+", " ", normalized, flags=re.IGNORECASE)
    return " ".join(normalized.split())
